WHO ARE WE ?
<BR>
We define ourselves by the equation
Karmavesh = karma +avesh
<BR>
<BR>
As described in the teaching of great leader, orator, and author of karmyog. Shri Swami Vivekananda, which defines to harness the power residing amongst the youth of nation.
<BR>
<BR>
Karmavesh encircle all the actions that leads towards the acquiring of a collective positive goal for the betterment of the students to identify their assets and to bring out their talent on a platform we provide them with
Our soul purpose is to advocate a positive impact on student which helps to identify themselves and pave to opportunity to prove their talent on significant dais. We work “along” with our students and believe in efficient team work. We provide them with opportunities which can lead them to become a better person be it social or cultural rostrum.
<BR>
<BR>
And thus, we at Karmavesh pledge to prove our enthusiasm towards the amelioration of all the students belonging to the institution.



